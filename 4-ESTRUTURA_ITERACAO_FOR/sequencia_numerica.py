# O loop for em Python é uma estrutura de repetição
# usada para iterar sobre sequências (listas, tuplas, strings) 
# ou objetos iteráveis. A sintaxe for item in sequencia: executa 
# um bloco de código recuado (indentado) para cada elemento. 
# É ideal para percorrer coleções de dados de forma clara e concisa.

nomes = ['anady', 'soph', 'fernando', 'socorreth']

for i in nomes:
  print(i)
  
for n in range(5):
  print(n)
  
for x in range(0, 11, 2):
  print(x)
  
produto = 'mouse'

for m in produto:
  print(m)